% g <= 0.
function [g, dg] = example_g(x, a, b)
  x1 = x(1); x2 = x(2);
  % -x2 <= 0.
  g = zeros(3, 1);
  g(1) = -x2;
  % (a1x1+b1)^3-x2<=0.
  a1 = a(1); b1 = b(1); a2 = a(2); b2 = b(2);
  g(2) = (a1 * x1 + b1)^3 - x2;
  % (a2x1+b2)^3-x2<=0.
  g(3) = (a2 * x1 + b2)^3 - x2;
  dg = zeros(3, 2);
  dg(1, :) = [0, -1];
  dg(2, :) = [3 * (a1 * x1 + b1)^2 * a1, -1];
  dg(3, :) = [3 * (a2 * x1 + b2)^2 * a2, -1];
end